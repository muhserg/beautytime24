<br><br> Вина за приведенные нарушения лежит всецело на ответственном за электрохозяйство СНТ "Опыт", Семенове Александре Викторовиче (участок 15). Собственно, из-за позиции его адвокатов и проведена данная судебная экспертиза.

    <br><br>В целом, СНТ Опыт вместо того, чтобы согласиться с крупнейшей энергокомпанией Подмосковья ПАО МОЭСК, <b>затеяло долгую судебную тяжбу, оплату которой повесят на всех членов СНТ</b>.

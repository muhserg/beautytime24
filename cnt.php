<h1>Пояснение автора.</h1>

Необходимо подчеркнуть, что автор борется не за то, чтобы, подключить 10 кВт себе, а за то, <b>чтобы ВСЕ члены СНТ могли подключить себе 10 кВт мощности</b>.
ПАО МОЭСК утверждает, что это технически возможно.

<br>Наилучшем же решением, будет <b>передать сети СНТ на баланс МОЭСК</b> (Домодедовские РЭС согласны), и тогда, при недостатке мощности, можно потребовать от МОЭСК установки более мощного трансформатора.


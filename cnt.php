<h1>Описание ситуации.</h1>

<p>
    На собрании СНТ «Опыт», весной 2018 года, председателем СНТ была публично озвучена необходимость установки приборов учета на границе земельного участка собственника.
    Было сказано, что собственник участка обязан за свой счет выполнить данную установку либо неофициально, за 15 тысяч рублей наличными без какого-либо договора, либо официально, через ПАО МОЭСК.
<br><br>
    В соответствии с данным указанием, я, Комаров Сергей Юрьевич, выбрал второй вариант и 06.07.2018 заключил договор с ПАО МОЭСК об осуществлении технологического присоединения (далее, ТП) к электрическим сетям №Ю8-18-302-9382(976904). 01.08.2018 ко мне на объект присоединения приехала монтажная группа подрядчика ПАО МОЭСК - ООО “Домсервис” (ОГРН 1135009004789), которая выполнила технические условия (далее, ТУ). Затем, с филиалом ПАО МОЭСК, Домодедовскими РЭС (далее, ДРЭС) были подписаны акты о выполнении ТУ, об опломбировании прибора учета и о допуске его в эксплуатацию.
    <br><br>
    24.08.2018 от начальника ДРЭС Кузнецова А. П. было отправлено письмо председателю СНТ №147/18 от 17.08.2018 о необходимости фактического присоединения моих энергопринимающих устройств к электросетям СНТ. В качестве реакции на письмо ПАО МОЭСК, мне 05.09.2018 было отправлено письмо от председателя СНТ, в котором в ТП мне было отказано, ссылаясь на техническую невозможность предоставления указанной в ТУ №Ю8-18-202-10942(976904/102/Ю8) мощности (10 кВт). При этом сам Семенов А. В. подключил свой участок трехфазным током с мощностью 23 кВт (имеются фотографии подключения - внизу данной страницы).
    <br><br>
    Данная причина отказа мне в ТП, по мнению начальника ДРЭС Кузнецова А. П., является несостоятельной, так как существующие сети СНТ обеспечивают подключение всех членов СНТ по ТУ №Ю8-18-202-10942(976904/102/Ю8). Также является несостоятельным заявление Семенова А. В. о невыплате мной членских, целевых взносов. Все взносы были оплачены в срок безналичным расчетом на расчетный счет СНТ «Опыт», что подтверждается выписками с моего расчетного счета. Ранее, в течение более 30 лет, все взносы оплачивались моим наследодателем, членом СНТ «Опыт», Комаровым Юрием Ильичом.
    <br><br>
    Таким образом, правление СНТ «Опыт», сначала публично обязало меня вынести прибор учета за границы земельного участка, что обошлось мне в сумму 38719 руб. 63 коп., а затем без адекватных причин отказало мне в подключении к своим сетям, кулуарно объясняя это тем, что надо было подключаться к электросетям неофициально, передав одному из членов правления СНТ 15 тысяч рублей наличными.  Данные действия правления СНТ подпадают под состав ст. 159 УК РФ, ст. 163 УК РФ и ст. 9.21 КоАП.
    <br><br>
    Для защиты своих прав, я, Комаров С.Ю. обратился в Домодедовский городской суд (<a href="/cnt/isk.docx">исковое заявление</a>). В качестве соистца выступила ПАО МОЭСК.
    <br><br>
    На предварительную беседу и на первое заседание отвечик не явился без уважительной причины.
    <br><br>
    На втором заседании отчетчик - СНТ Опыт явился в лице Гаврилиной Т.В., Семенова А.В., и нанятого юриста.
    Юрист попросил отложить заседание, на основании того, что СНТ не было уведомлено о заседании суда.
    <br><br>
    На третьем заседании нанятый СНТ юрист и нанятый СНТ адвокат высказали свои доводы суду,
    однако суд счел их доводы несостоятельными и предложил провести СНТ экспертизу по электроснабжению  за свой счет.
    <br><br>
    На четвертое заседание отвечик не явился без уважительной причины.
    <br><br>
    На пятом заседании СНТ представило свою экспертизу, сделанную за счет СНТ, но суд не счел ее удовлетворительной и назначил свою судебную экспертизу. Проведенная судебная экспертиза №097-19/Э показала серьезные нарушения в распределительном устройстве трансформаторной подстанции СНТ “ОПЫТ”. В частности:
<br>1)	на стр. 9 экспертизы, предпоследний абзац, сказано следующее: "Установлено, что суммарная сила тока автоматических выключателей (160+160+100+80 = 540А) на отводах фидеров превышает нормативную силу тока, выдаваемую исследуемым трансформатором";
<br>2)	на стр. 10, п2, следующее: “Поверхность монтажная щита 0,4 кВ распределения и учета СНТ “Опыт” в месте монтажа прибора учета, испытательной коробки содержит следы термического воздействия, свидетельствующие о имевшей ранее быть аварийной ситуации”;
<br>3)	на стр.16, абзац 2: “В результате проведенных изменений установлено, что шины токопроводящие от основного рубильника на фазе 1 подвергаются значительному нагреву в результате прохождения электрического тока, в диапазоне от 54,6 до 88,1 0С”.
<br>4)	на стр. 20, абзац 1: "оборудование на щите 0,4 кВ находится в работоспособном состоянии, граничащим с аварийным, требуется проведение мероприятий по нормализации потребления электрической мощности по фазам";
